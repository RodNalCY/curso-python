'''
  > Mayor que
  < Menor que
 >= mayor o igual que
 <= menor o igual que
 != diferente
 == igual
'''
resultado = 10 < 20
print(resultado)

resultado = 10 > 20
print(resultado)

resultado = 10 == 20
print(resultado)

resultado = 10 != 20
print(resultado)

resultado = 10 <= 20
print(resultado)

resultado = 10 >= 20
print(resultado)

